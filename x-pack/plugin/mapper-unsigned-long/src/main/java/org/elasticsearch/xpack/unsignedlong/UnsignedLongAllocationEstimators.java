package org.elasticsearch.xpack.unsignedlong;

import java.math.BigInteger;

/**
 * {@code @allocates} estimators for the {@code asBigInteger} field accessors in this module's allowlist. Each is a
 * {@code public static long} method matching the allowlisted instance method's Java signature (receiver first) and returns
 * the heap cost of the {@link BigInteger} it allocates. Invoked from generated bytecode on the allocation pre-check path, so
 * they must be cheap, must not allocate, and must not throw.
 *
 * <p>An unsigned long fits in a 64-bit {@code BigInteger}, so the result is a fixed-size object plus a one- or two-element
 * {@code int[]} magnitude — a constant charge regardless of the argument values.
 */
public final class UnsignedLongAllocationEstimators {

    /** Heap cost of a {@link BigInteger} holding a value that fits in 64 bits: the object plus its small backing {@code int[]}. */
    private static final long UNSIGNED_LONG_BIG_INTEGER_BYTES = 48;

    private UnsignedLongAllocationEstimators() {}

    /** {@code UnsignedLongDocValuesField.asBigInteger(BigInteger)}: a single ≤64-bit {@link BigInteger}. */
    public static long asBigIntegerBytes(UnsignedLongDocValuesField receiver, BigInteger defaultValue) {
        return UNSIGNED_LONG_BIG_INTEGER_BYTES;
    }

    /** {@code UnsignedLongDocValuesField.asBigInteger(int, BigInteger)}: a single ≤64-bit {@link BigInteger}. */
    public static long asBigIntegerBytes(UnsignedLongDocValuesField receiver, int index, BigInteger defaultValue) {
        return UNSIGNED_LONG_BIG_INTEGER_BYTES;
    }
}
