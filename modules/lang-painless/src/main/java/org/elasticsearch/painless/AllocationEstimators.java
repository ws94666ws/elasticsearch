package org.elasticsearch.painless;

import java.util.Collection;

/**
 * Built-in {@code @allocates_dynamic} estimators: {@code public static long} methods matching the annotated target's full Java
 * signature (receiver first for instance methods), returning the total heap cost of the call. Invoked from generated bytecode
 * on the pre-check path, so they must be cheap, must not allocate, and <b>must not throw</b> — even on argument values the
 * annotated call itself would reject. Results are normalized by {@link AllocationGuard#sanitizeEstimate(long)}.
 */
public final class AllocationEstimators {

    private AllocationEstimators() {}

    /**
     * Cost of {@code String.substring(begin, end)}: result overhead plus {@code end - begin} chars at 2 bytes each (UTF-16
     * worst case). An inverted range costs just the overhead; the real call rejects invalid indices after the pre-check.
     */
    public static long substringBytes(String receiver, int begin, int end) {
        long chars = Math.max(0L, (long) end - begin);
        return AllocSizes.STRING_CONCAT_RESULT_OVERHEAD + AllocSizes.mulSat(2L, chars);
    }

    /**
     * Cost of {@code new ArrayList(collection)}: the list shell plus a backing {@code Object[]} sized to the source. A
     * {@code null} source costs just the shell; the real constructor rejects the {@code null} after the pre-check.
     */
    public static long arrayListCollectionBytes(Collection<?> collection) {
        long size = collection == null ? 0 : collection.size();
        return ARRAY_LIST_SHELL_BYTES + AllocSizes.arrayBytes(size, AllocSizes.REFERENCE_SIZE);
    }

    /**
     * Heap cost of an {@code ArrayList} instance excluding its backing array; matches the {@code @allocates_constant} value on
     * the no-arg {@code new ArrayList()}, whose backing array is lazily created.
     */
    static final long ARRAY_LIST_SHELL_BYTES = 40;
}
